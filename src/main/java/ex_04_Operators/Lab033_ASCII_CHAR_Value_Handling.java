package ex_04_Operators;

public class Lab033_ASCII_CHAR_Value_Handling {
    public static void main(String[] args) {
        char a1 = 'A'; //  ASCII - 65
        char a2 = 'B'; //  ASCII - 66
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a1+a2); // when any arithmetic operation is performed on characters they get replace by their ASCII values
        System.out.println(a1-a2);
        // char are noting but int :D
    }

}
